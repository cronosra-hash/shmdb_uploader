def validate_episode_ids(episode_ids, output_path="missing_episode_ids.txt"):
    conn = get_connection()
    missing_ids = []

    with conn.cursor() as cur:
        for eid in episode_ids:
            cur.execute("SELECT 1 FROM episode WHERE episode_id = %s;", (eid,))
            if not cur.fetchone():
                missing_ids.append(eid)

    conn.close()

    if missing_ids:
        print(f"❌ Missing {len(missing_ids)} episode IDs. Writing to {output_path}...")
        with open(output_path, "w", encoding="utf-8") as f:
            f.write("Missing Episode IDs:\n")
            for eid in missing_ids:
                f.write(f"{eid}\n")
    else:
        print("✅ All episode IDs exist in the database.")
        with open(output_path, "w", encoding="utf-8") as f:
            f.write("All episode IDs are present.\n")

    return missing_ids

